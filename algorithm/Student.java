package algorithm;

public class Student {
	private int no;
	private String name;
	
	public Student(String name, int no) {
		super();
		this.name = name;
		this.no = no;
		
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	


	}
